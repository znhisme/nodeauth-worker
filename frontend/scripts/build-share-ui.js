import { mkdirSync, readFileSync, writeFileSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const root = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const outDir = resolve(root, 'dist/assets');

mkdirSync(outDir, { recursive: true });

const sources = [
    ['share-management-ui.js', 'src/share-ui/share-management-ui.js'],
    ['share-management-ui.css', 'src/share-ui/share-management-ui.css'],
];

for (const [outName, sourcePath] of sources) {
    const source = readFileSync(resolve(root, sourcePath), 'utf8');
    writeFileSync(
        resolve(outDir, outName),
        `/* Generated from frontend/${sourcePath}. Do not edit this file directly. */\n${source}`,
    );
}

console.log('Built share management UI overlay.');

